# 项目架构与技术选型（修订版）

## 1. 概述

本项目《孵化终焉：触变边界》是一款融合了视觉小说、世界探索、生存恐怖与战斗生存元素，并包含成人向内容的PC平台游戏。根据用户最新需求，项目素材主要为2D，但H场景需要格外精细，并可能涉及3D动画。这意味着项目架构需支持2D与3D内容的无缝融合，并对H场景的表现力有特殊考量。本修订版将详细阐述项目架构设计和技术选型，以适应这一新的需求。

## 2. 核心技术选型：游戏引擎

考虑到项目主体为2D视觉小说，但H场景对精细度和3D动画的需求，选择一个能良好支持2D和3D混合开发的引擎至关重要。**Unity** 在此方面具有显著优势。

### 2.1 Unity

*   **优势**：
    *   **卓越的2D工具集**：Unity拥有强大的2D渲染管线（如Sprite Renderer, Tilemap）、2D物理系统、2D动画工具（Sprite Editor, Animator），非常适合制作高质量的2D视觉小说和探索场景。
    *   **灵活的2D/3D混合开发**：Unity能够轻松地在同一项目中集成2D和3D内容。这意味着可以在2D背景上叠加3D角色或特效，或者在H场景中切换到全3D渲染，实现无缝衔接。
    *   **丰富的动画系统**：Unity的Animator系统可以管理2D骨骼动画（配合Spine或Live2D插件效果更佳）和3D模型动画。对于H场景的3D动画，可以直接导入外部制作的3D模型和动画，或在Unity内部进行动画编辑。
    *   **强大的渲染管线**：URP（通用渲染管线）和HDRP（高清渲染管线）提供了灵活的渲染选项，可以为2D和3D内容分别优化，实现高质量的视觉效果。
    *   **成熟的生态系统**：庞大的Asset Store和社区资源，可以找到大量用于视觉小说、2D动画、3D模型导入和动画、后处理特效等方面的插件和解决方案，大幅加速开发。
    *   **Windows可执行程序**：Unity可以轻松构建独立的Windows .exe文件，并包含所有必要的DLLs，满足用户对Windows端可执行程序的需求。
    *   **C#编程语言**：相对容易上手，拥有庞大的开发者社区和丰富的学习资源。

*   **劣势**：
    *   对于电影级别的超高精度3D渲染，可能需要更多定制化开发或不如Unreal Engine开箱即用。

### 2.2 Unreal Engine (虚幻引擎) - 备选

*   **优势**：
    *   **顶级3D渲染能力**：如果H场景的3D动画要求达到电影级别，Unreal Engine的渲染能力无疑是顶尖的。
    *   **蓝图可视化编程**：对于复杂的3D动画逻辑和特效，蓝图系统可以提高效率。

*   **劣势**：
    *   2D开发流程相对不如Unity成熟和便捷，可能需要更多自定义工作。
    *   学习曲线陡峭，项目体积和运行时资源占用通常较大。

**结论**：综合考虑，**Unity** 是更适合本项目需求的引擎选择。它在2D开发上的优势能确保视觉小说主体部分的效率和质量，同时其强大的3D集成能力也能满足H场景对精细3D动画的需求。

## 3. 项目架构设计（修订版）

项目架构将继续遵循模块化、可扩展的原则，但会更明确地划分2D和3D内容的管理。

### 3.1 表现层 (Presentation Layer)

*   **UI/UX 系统**：
    *   **视觉小说界面**：对话框、文本框、选项菜单、CG画廊、日志系统。这些将主要以2D UI元素实现。
    *   **游戏HUD**：生命值、精神状态、变异进度、技能冷却、资源显示。同样以2D UI为主。
    *   **主菜单/暂停菜单**：设置、存档/读档、退出、新游戏。
    *   **H事件UI**：特殊交互提示、快感/痛苦指示器、变异提示。可能需要与3D H场景进行深度融合。
*   **场景管理**：
    *   **2D背景管理**：加载和切换2D背景图，支持视差滚动等效果。
    *   **2D角色立绘管理**：根据剧情和H事件状态，动态加载和切换2D角色立绘（包括不同表情、姿态、变异阶段）。
    *   **H场景3D环境/角色管理**：在进入H事件时，无缝切换或叠加3D场景和3D角色模型，管理其加载、渲染和卸载。
*   **动画与特效**：
    *   **2D角色动画**：主角、敌人、NPC的2D骨骼动画（行走、攻击、受击、变异动画）。
    *   **3D H场景动画**：H事件中主角、敌人、触手等关键元素的3D动画，包括身体变形、异种交融、处决动画等，要求极高精细度。
    *   **环境特效**：生物电流、体液粘连、毒雾、菌丝生长、机械运作等，可结合2D粒子系统和3D粒子系统实现。
    *   **H事件特效**：特殊视觉反馈，强化猎奇感，可能需要结合3D渲染技术。

### 3.2 游戏逻辑层 (Game Logic Layer)

*   **核心游戏循环**：状态机管理（包括2D视觉小说状态和3D H场景状态的切换）、事件系统。
*   **角色系统**：
    *   **主角管理**：生命值、精神值、体力、变异状态、技能、物品栏。需要同步管理2D立绘和3D模型的状态。
    *   **身体演化树**：管理变异分支、解锁新能力、属性加成。影响2D立绘和3D模型的形态。
    *   **H状态影响**：根据H事件中的表现和状态调整角色属性和行为。与3D H场景的动画逻辑紧密结合。
*   **敌人/NPC 系统**：
    *   **AI行为**：巡逻、追击、攻击、特殊能力。对于2D敌人，行为在2D平面上；对于3D H场景中的敌人，行为在3D空间中。
    *   **怪物H→杀戮流程**：管理H场景触发、3D动画播放、处决动画逻辑。
*   **战斗系统**：
    *   **实时近战+技能施放**：主要在2D或2.5D视角下进行，但技能特效可能包含3D元素。
*   **剧情与任务系统**：对话管理、任务管理、结局判定。
*   **资源管理**：物品管理、重生机制、重生点传送。
*   **心理状态与幻觉系统**：精神值管理、幻觉触发。幻觉表现可能结合2D和3D特效。

### 3.3 数据层 (Data Layer)

*   **游戏数据**：配置数据、剧情数据、场景数据（包括2D和3D场景数据）。
*   **存档/读档系统**：保存玩家进度、角色状态、物品、已解锁内容。
*   **本地化数据**：文本、语音等。

## 4. 开发语言与工具（修订版）

### 4.1 主要开发语言

*   **Unity**：**C#**。所有游戏逻辑、UI交互、系统实现都将主要使用C#编写。

### 4.2 美术与动画工具

*   **2D美术/概念美术**：Photoshop, Clip Studio Paint, Procreate (iPad)。
*   **2D骨骼动画**：Spine, Live2D (用于制作精细的2D角色立绘动画，特别是表情和身体微动)。
*   **3D建模/雕刻**：Blender, ZBrush, Maya, 3ds Max (用于H场景中的角色、触手、环境元素的3D模型制作，要求高精度)。
*   **纹理绘制**：Substance Painter, Substance Designer (用于3D模型的PBR纹理制作)。
*   **3D动画**：Maya, 3ds Max, Blender (用于制作H场景的3D动画，包括骨骼绑定、蒙皮、关键帧动画、动力学模拟等)。
*   **视频编辑/合成**：After Effects, Premiere Pro (如果H场景采用预渲染视频，则用于后期合成)。
*   **UI设计**：Adobe XD, Figma, Sketch。

### 4.3 音效与音乐工具

*   **数字音频工作站 (DAW)**：Ableton Live, FL Studio, Logic Pro, Cubase。
*   **音效库**：各种商业或免费音效库，用于“有机声波、融合噪音、失真呻吟”等氛围音效。
*   **音频中间件**：FMOD, Wwise (用于复杂音效管理和交互，特别是与3D H场景的音效同步)。

### 4.4 版本控制

*   **Git**：配合Git LFS (Large File Storage) 管理大型美术资源（包括2D图集和3D模型/动画文件），确保团队协作效率和版本回溯能力。

## 5. Windows可执行程序生成

Unity提供了完善的打包和发布流程，可以直接生成Windows平台下的独立可执行程序（.exe文件）以及所需的动态链接库（.dll文件）。在Build Settings中选择PC, Mac & Linux Standalone平台，目标平台选择Windows，然后点击Build即可生成包含游戏主程序、数据文件和必要DLLs的文件夹。

## 6. 总结

本项目修订后的实现计划，将以**Unity**作为主要开发引擎，充分利用其在2D开发上的优势，并结合其强大的3D集成能力来满足H场景对精细3D动画的需求。项目架构将更明确地划分2D和3D内容的管理，确保两者无缝融合。美术工具链将涵盖2D手绘、2D骨骼动画、3D建模、3D动画和PBR纹理制作，以支持高质量的视觉表现。最终，通过Unity的打包功能，可以轻松生成满足用户需求的Windows可执行程序。

