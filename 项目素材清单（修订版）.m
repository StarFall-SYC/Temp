# 项目素材清单（修订版）

根据用户对项目素材的最新要求，特别是强调2D素材、H场景的精细度以及可能需要的3D动画，以下是《孵化终焉：触变边界》项目所需的主要素材类型。

## 1. 角色素材

*   **主角**：
    *   **2D立绘**：正常形态、不同情绪、姿态的精细2D立绘。需要多套表情和服装/身体状态（如轻微变异）。
    *   **2D骨骼动画（Spine/Live2D）**：用于2D立绘的微动、呼吸、表情变化、简单的肢体动作，以增强视觉小说的表现力。
    *   **H事件3D模型与动画**：针对H事件，主角需要高精度的3D模型，包含骨骼绑定和精细的蒙皮。动画将涵盖各种H交互动作、身体变形、异种交融等，要求动作流畅、细节丰富。
    *   **战斗姿态与技能特效**：2D战斗姿态，技能特效可结合2D粒子和少量3D元素。
*   **敌人/NPC**：
    *   **2D立绘/骨骼动画**：各区域敌人的精细2D立绘，部分可能需要2D骨骼动画来表现蠕动、攻击前摇等。
    *   **H事件3D模型与动画**：针对H事件，部分关键敌人（如蛛后、机甲殖婴、巨型菌林母虫）需要高精度的3D模型和动画，用于H交互和处决动画。这些3D模型将与主角的3D模型进行互动。
    *   **处决动画3D预渲染视频/实时动画**：每种怪物独特的H→Kill动画流程，可能以高精度的3D预渲染视频形式呈现，或在引擎中实时播放3D动画。

## 2. 场景素材

*   **2D背景图**：
    *   **孵化舱区**：黏滑潮湿的孵化舱内部、生物组织与机械共生场景。需要多张不同角度或细节特写的背景图。
    *   **虫巢都市区**：废弃公寓、菌丝虫群侵蚀的街道、阴影重重的室内。强调破败、阴森的氛围。
    *   **异形工厂区**：冷酷金属、机械胚胎、酸液管道、生产线。强调工业、异形的结合。
    *   **毒雾菌林区**：巨大菌丝树、毒孢密布的地面、发酵空气中的菌林。强调诡异、压抑的自然环境。
    *   **深层实验室区**：神经共振中心、血肉回路、机械包围的实验台。强调科技与血肉的融合。
*   **场景元素（2D或少量3D）**：
    *   可交互物体（样本、资源点、解密机关）：主要为2D贴图，部分关键交互物可制作简单3D模型。
    *   重生点（孵化节点）：2D图标或带有发光特效的2D精灵。
    *   环境特效（生物电流、体液粘连、毒雾、菌丝、酸液、幻觉效果）：可结合2D粒子系统、Shader特效和少量3D粒子系统实现。
*   **H场景专属3D环境**：针对H事件，可能需要独立的3D小场景或局部3D环境，以配合3D角色动画，增强沉浸感。

## 3. UI/UX 素材

*   **通用UI（2D）**：
    *   对话框、文本框、选项按钮、主菜单、暂停菜单、设置界面、存档/读档界面、物品栏、技能树界面。所有UI元素均为2D图像和文本。
*   **HUD元素（2D）**：
    *   生命值、精神值、体力条、变异进度条、技能冷却图标、资源图标。均为2D图形和数字显示。
*   **H事件UI（2D，与3D场景融合）**：
    *   快感/痛苦指示器、失控阶段提示、特殊交互按钮。这些UI元素需要能够无缝叠加在H场景的2D背景或3D渲染之上。

## 4. 特效素材

*   **战斗特效（2D为主，少量3D）**：
    *   近战攻击特效、受击特效：2D精灵动画或粒子特效。
    *   技能施放特效（虫肢打击、毒腺喷射、腐蚀云、精神腐蚀雾）：可结合2D精灵动画、2D粒子系统，对于“腐蚀云”或“精神腐蚀雾”等效果，可考虑使用3D粒子系统或体积特效。
*   **H事件特效（2D与3D结合）**：
    *   触手缠绕、卵植入、肢解爆体等视觉表现：这些是H场景的核心视觉，将主要通过3D动画和3D特效（如流体模拟、破碎效果）来实现，并可能结合2D后处理效果。
    *   精神幻觉、欲望幻觉的视觉扭曲效果：可通过Shader特效、2D后处理滤镜、或结合少量3D扭曲效果实现。
*   **环境特效（2D与3D结合）**：
    *   毒雾弥漫、菌丝生长、生物电流闪烁：可使用2D粒子系统、Shader动画，或在特定区域使用3D体积雾、3D粒子系统来增强真实感。

## 5. 音效素材

*   **环境音效**：各区域独特的环境音效（黏滑潮湿、菌丝蠕动、机械运转、酸液滴落、毒雾嘶嘶声）。
*   **角色音效**：主角的呻吟、喘息、变异时的痛苦或快感声音；敌人的嘶吼、蠕动、攻击音效。
*   **H事件音效**：触手摩擦、体液粘连、骨骼变形、肉体撕裂、卵破裂等猎奇音效；高潮、虚弱、失控阶段的特殊音效。这些音效需要与H场景的3D动画精确同步。
*   **UI音效**：按钮点击、菜单切换等。
*   **战斗音效**：武器打击、技能施放等。

## 6. 音乐素材

*   **背景音乐**：探索、战斗、H事件、剧情高潮、不同结局的背景音乐。强调氛围感和情绪渲染。

## 7. 动画素材

*   **2D角色动画**：行走、奔跑、潜行、攻击、受击、死亡（使用2D骨骼动画或逐帧动画）。
*   **H事件3D动画**：主角与敌人/触手的H交互动画，身体变形动画，异种交融动画。这是项目视觉表现的重中之重，需要最高精细度。
*   **怪物处决3D动画/预渲染视频**：每种怪物独特的H→Kill动画流程，可以是实时3D动画或高质量的预渲染视频。
*   **UI动画**：菜单过渡、按钮反馈等（2D动画）。

## 素材准备策略

1.  **2D美术为主，3D为辅**：大部分场景、UI和非H事件的角色将采用2D美术资源。精细的2D立绘和背景是视觉小说的核心。
2.  **H场景的3D高精细度**：H事件将是3D模型和动画的重点投入区域。需要专业的3D建模师和动画师制作高面数模型、精细纹理和流畅动画。
3.  **AI辅助与人工精修结合**：对于2D概念图和3D模型的基础形态，可以继续利用AI工具辅助生成，但最终的精修、细节调整、风格统一和动画制作必须由专业美术人员完成。
4.  **音效与动画同步**：H场景的音效和3D动画需要紧密配合，确保视听体验的冲击力。
5.  **程序化生成辅助**：继续利用脚本生成程序化纹理、粒子特效和UI布局，以提高效率和保持风格一致性。

