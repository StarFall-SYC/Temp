
# 🎮 项目名称（暂定）：孵化终焉：触变边界（Incubation’s End: The Touched Veil）

---

## 一、项目概述  
**类型**：成人向（18+）视觉小说／世界探索／生存恐怖／战斗生存  
融合猎奇血腥暴力、异形科幻与性恐惧元素，主打人体变异、异种交融，通过H事件与战斗推进剧情与角色演化。

---

## 二、游戏世界与探索区域  
> 以下五大区域构成完整探索流程，每个区域包含敌人与H触发、剧情节点、战斗资源与重生点分布。

### 1. 孵化舱区（Bio-Hatch Chambers）  
- **环境**：黏滑潮湿，漂浮孵化舱与生物组织和机械共生。  
- **剧情节点**：主角被派遣清理失控实验，揭示记忆不可靠。  
- **敌人与异变**：  
  - 半虫科研人，虫腿刺破脊椎；  
  - “蛆孵女”，蛆虫遍布腹部。  
- **玩法与H**：  
  - 探索、潜行、样本收集；  
  - 触手H交互与变异诱导；  
  - 道德抉择影响身体与心理状态。

---

### 2. 虫巢都市区（Infested Borough）  
- **环境**：废弃公寓被菌丝虫群侵蚀，阴影重重。  
- **剧情节点**：揭露“人类孵化计划”，社会沦陷。  
- **敌人与异变**：  
  - 人虫混合体，胸甲附体；  
  - 蛛后，催乳与心灵操控兼具。  
- **玩法与H**：  
  - 战略诱捕或引爆；  
  - 心理幻觉与欲望虐妄想；  
  - 蛛后H交互、虫卵植入触发强力肉体变异。

---

### 3. 异形工厂区（Xeno-Factory）  
- **环境**：冷酷金属与机械胚胎交错，管道中夹带酸液。  
- **剧情节点**：深入核心，发现人体–机械–外星杂交实验。  
- **敌人与异变**：  
  - 机能失衡的工人异形混合体；  
  - “机甲殖婴”，可战斗又可H。  
- **玩法与H**：  
  - 解密、潜行、资源改造；  
  - 机肉融合交互，音效强化病态感；  
  - 身体自定义，可获得机械触手、毒腺等。

---

### 4. 毒雾菌林区（Spore‑Fog Forest）  
- **环境**：巨大菌丝树，地面毒孢密布，空气发酵。  
- **剧情节点**：调查污染源，寻找母虫群巢穴。  
- **敌人与异变**：  
  - 巨型菌林母虫，催眠与菌液注射；  
  - 进入毒雾后出现H幻觉。  
- **玩法与H**：  
  - 呼吸管理、防护设备；  
  - 气体触发幻觉/欲望H画面；  
  - 共生系统，锁定变异结局。

---

### 5. 深层实验室区（Black Labs）  
- **环境**：神经共振中心，血肉回路机械包围。  
- **剧情节点**：最终对决，净化组织暴露真相。  
- **敌人与异变**：  
  - 精神共振净化者；  
  - 脑壳裂开的意识融合怪。  
- **玩法与H**：  
  - 意识层面性的对抗机制；  
  - 精神/肉体融合或净化路径选择；  

