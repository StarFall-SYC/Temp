## 六、结局与分支  
聚焦游戏流程与探索体验：

- **融合终焉**（主流推进结局）：被异种吸收，获得力量但失去人性。  
- **净化觉醒**（稀有达成条件）：保留意志，净化异种，结局带有牺牲。  
- **重生循环**（战败但未毁灭）：触发主角多次死亡与复活，形成永劫循环。

