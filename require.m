## 三、系统概览（扩展）  

| 系统模块             | 描述 |
|----------------------|------|
| **身体演化树**          | 自定义变异分支，提升战斗能力、H适应性与视觉差异 |
| **H‑驱动力系统**       | 每个区域存在不同触发方式，主角在H事件中可表现抗拒或沉溺，影响分支与变异倾向 |
| **战斗机制**           | 实时近战+技能施放系统，分为基础攻击与变异技能（如虫肢打击、毒腺喷射） |
| **技能进阶系统** | 技能可升级为更高形态（如：毒喷→腐蚀云→精神腐蚀雾），战斗风格可自定义偏向群体控制/单体爆发/吸收回复等 |
| **H状态影响系统** | H事件中主角可进入“失控阶段”、“高潮后虚弱”、“快感压制”等状态，影响移动速度、战斗输出、技能冷却等，增加策略性与代价感 |
| **怪物H→杀戮流程** | 每种怪物拥有独立的H场景+处决动画流程（如蛛后：蛛丝缠绕→卵植入→肢解爆体），呈现完整猎杀链条 |
| **怪物处决动画系统**     | 每次H场景结束后，怪物可能进入“猎杀阶段”，播放独特杀戮动画，玩家死亡后于最近重生点复活 |
| **重生机制**           | 玩家可激活“孵化节点”作为重生点，死亡后于最近激活点复活，并保留部分资源 |
| **重生点传送功能** | 可通过菜单或场景终端传送至已激活过的其他重生点，支持区域快速回溯与支线任务探索 |
| **心理状态与幻觉**       | 不同幻觉影响战斗表现、对H反应与世界认知，存在随机/剧本驱动两类幻觉 |
| **视觉氛围营造**        | 生物电流、体液粘连、低频鼓膜压迫等动态音效系统，强化猎奇与沉浸感 |

