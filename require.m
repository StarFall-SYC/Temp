## 四、核心玩法流程  

1. **启动任务（孵化舱区）**  
   - 教学任务，初步战斗/H接触，激活第一个重生点。
2. **城市探索（虫巢都市）**  
   - 增强战斗技巧与技能进阶，进入H沉溺/抗拒分支，面对初次怪物杀戮动画。
3. **工业改造（工厂区）**  
   - 解锁技能树与状态系统，首次体验H后虚弱状态对战斗影响。
4. **菌林生存（菌林区）**  
   - 高密度H诱导区域，需交替控制幻觉与状态，合理利用传送重生点规避追击。
5. **最终对抗（实验室区）**  
   - BOSS具有完整H→处决流程，技能与状态系统全面融合，完成多阶段决战。

