# 《孵化终焉：触变边界》Unity项目

## 项目概述
这是一个使用Unity 2022.3.61f1c1开发的游戏项目，融合视觉小说、探索和H场景3D动画。

## 结构
- **Assets/**: 资源文件夹，包括Scripts、Scenes等。
- **Packages/**: 依赖管理。
- **ProjectSettings/**: 项目设置，包括版本。
- **Library/**: Unity生成的库。
- **UserSettings/**: 用户设置。

## 如何运行
1. 安装Unity 2022.3.61f1c1。
2. 打开此文件夹作为Unity项目。
3. 在Unity Editor中构建为Windows可执行文件。